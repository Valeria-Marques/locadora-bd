    private void btPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisaActionPerformed
        
        String pesquisa = jTF_CodDVD.getText();
        Connection con = (Connection) Conexao.AbrirConexao();
        if (pesquisa.equals("")) {
            
            JOptionPane.showMessageDialog(null, "Digite o codigo do DVD", "Video Locadora", JOptionPane.WARNING_MESSAGE);
        
        } else {
            
            DVDDAO sql = new DVDDAO(con);
            int cod = Integer.parseInt(pesquisa);
            
            if (sql.Testar_DVD(cod) == false) {
                
                JOptionPane.showMessageDialog(null, "Codigo do DVD não encontrado", "Video Locadora", JOptionPane.ERROR_MESSAGE);
                
                jTF_CodDVD.setText("");
                jTF_Titulo.setText("");
                jTF_Valor.setText("");
                jTF_Categoria.setText("");
                jTF_Classificacao.setText("");
                jTF_Codigo.setText("");
                jTF_CodigoCliente.setText("");
                jCB_Nome.setSelectedIndex(0);
                
                
            } else if (sql.Testar_Situacao(cod) == false ) {
             
                JOptionPane.showMessageDialog(null, "O DVD de codigo ( "+cod+" ) está "
                        + "emprestado", "Video Locadora", JOptionPane.INFORMATION_MESSAGE);
                
                jTF_CodDVD.setText("");
                jTF_Titulo.setText("");
                jTF_Valor.setText("");
                jTF_Categoria.setText("");
                jTF_Classificacao.setText("");
                jTF_Codigo.setText("");
                jTF_CodigoCliente.setText("");
                jCB_Nome.setSelectedIndex(0);
                
            } else {
                
                InserirDados(cod);
                jTF_Codigo.setText(pesquisa);
                jTF_CodDVD.setText("");
                
            }
            
        }
        
    }//GEN-LAST:event_btPesquisaActionPerformed

