    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        
        String dvd = jTF_Codigo.getText();
        String cliente = jTF_CodigoCliente.getText();
        String horario = jTF_Horas.getText();
        String alguel = jTF_DataLocacao.getText();
        
        if (dvd.equals("") || cliente.equals("") || alguel == null || jTF_DataDevolucao.getText().equals("")) {
            
            JOptionPane.showMessageDialog(null, "Nenhum campo pode estar vazio", "Video Locadora", JOptionPane.WARNING_MESSAGE);
            
        } else {
            
            String devolucao = jTF_DataDevolucao.getText();
            Connection con = (Connection) Conexao.AbrirConexao();
            AluguelDAO sql = new AluguelDAO(con);
            Aluguel a = new Aluguel();
            
            a.setCoddvd(Integer.parseInt(dvd));
            a.setCodcliente(Integer.parseInt(cliente));
            a.setHorario(horario);
            a.setDataAluguel(alguel);
            a.setDataDevolucao(devolucao);
            sql.Inserir_Aluguel(a);
            
            sql.Atualiza_Situacao(a);
            Conexao.FecharConexao(con);
            
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso", "Video Locadora", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            
        }
        
    }//GEN-LAST:event_btCadastrarActionPerformed

