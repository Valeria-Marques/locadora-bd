    private void ID() {
        Connection con = Conexao.AbrirConexao();
        ClassificacaoDAO sql = new ClassificacaoDAO(con);
        List<Classificacao> lista = new ArrayList<>();
        lista = sql.CodigoID();
            for (Classificacao c : lista) {
                int cod = c.getFCodigo()+1;
                WCodigo.setText("" + cod);
            }
            Conexao.FecharConexao(con);
    }
    
