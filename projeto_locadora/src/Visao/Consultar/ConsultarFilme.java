    private void AtualizarTable() {
        
        Connection con = (Connection) Conexao.AbrirConexao();
        FilmeDAO bd = new FilmeDAO(con);
        List<Filme> lista = new ArrayList<>();
        lista = bd.ListarFilme();
        DefaultTableModel tbm = (DefaultTableModel) jTable.getModel();
        
        while (tbm.getRowCount() > 0) {            
            tbm.removeRow(0);
        }
        
        int i = 0;
        for (Filme f : lista) {
            
            tbm.addRow(new String[i]);
            jTable.setValueAt(f.getCodigo(), i, 0);
            jTable.setValueAt(f.getTitulo(), i, 1);
            jTable.setValueAt(f.getAno(), i, 2);
            jTable.setValueAt(f.getDuracao(), i, 3);
            jTable.setValueAt(f.getCodigoCategoria(), i, 4);
            jTable.setValueAt(f.getCodigoClassificacao(), i, 5);
            i++;
            
        }
        Conexao.FecharConexao(con);
        
    }
    
