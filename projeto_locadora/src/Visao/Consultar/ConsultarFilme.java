    private void btPesquisaCodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisaCodActionPerformed
        
        Connection con = (Connection) Conexao.AbrirConexao();
        FilmeDAO bd = new FilmeDAO(con);
        List<Filme> lista = new ArrayList<>();
        String cod = jTF_Cod.getText();
        lista = bd.Pesquisa_Cod_Filme(Integer.parseInt(cod));
        DefaultTableModel tbm = (DefaultTableModel) jTable.getModel();
        
        while (tbm.getRowCount() > 0) {            
            tbm.removeRow(0);
        }
        
        int i = 0;
        for (Filme tab : lista) {
            tbm.addRow(new String[i]);
            jTable.setValueAt(tab.getCodigo(), i, 0);
            jTable.setValueAt(tab.getTitulo(), i, 1);
            jTable.setValueAt(tab.getAno(), i, 2);
            jTable.setValueAt(tab.getDuracao(), i, 3);
            jTable.setValueAt(tab.getCodigoCategoria(), i, 4);
            jTable.setValueAt(tab.getCodigoClassificacao(), i, 5);
            i++;
        }
        Conexao.FecharConexao(con);
        
    }//GEN-LAST:event_btPesquisaCodActionPerformed

