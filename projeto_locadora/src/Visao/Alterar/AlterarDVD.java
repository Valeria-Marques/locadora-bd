    private void InserirDados(int cod){
        
        Connection con = (Connection) Conexao.AbrirConexao();
        DVDDAO sql = new DVDDAO(con);
        List<DVD> lista = new ArrayList<>();
        lista = sql.CapturarDVD(cod);
        
        for (DVD a : lista) {
            
            jTF_Codigo.setText("" + a.getCodigo());
            jTF_PrecoCompra.setText(""+a.getPreco());
            jDC_Data_Compra.setText(a.getDataCompra());
            jTF_CodigoFilme.setText(""+a.getCodigoFilme());
            
        }
        
        Conexao.FecharConexao(con);
        
    }
    
    private void Limpar(){
        
        jTF_Codigo.setText("");
        jTF_CodigoFilme.setText("");
        jTF_PrecoCompra.setText("");
        jCB_Nome.setSelectedIndex(0);
        
    }
    
    private void AtualizaCombo() {
        
        Connection con = (Connection) Conexao.AbrirConexao();
        FilmeDAO sql = new FilmeDAO(con);
        List<Filme> lista = new ArrayList<Filme>();
        lista = sql.ListarComboFilme();
        //jCB_Nome.addItem("");
        
        for (Filme filme : lista) {
            
            jCB_Nome.addItem(filme.getTitulo());
            
        }
        
        Conexao.FecharConexao(con);
        
    }

