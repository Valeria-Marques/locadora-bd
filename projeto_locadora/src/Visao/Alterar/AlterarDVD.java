    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        
        String codigo = jTF_Codigo.getText();
        String situacao = jTF_Situacao.getText();
        String codFilme = jTF_CodigoFilme.getText();
        String precoCompra = jTF_PrecoCompra.getText();
        String dataCompra = jDC_Data_Compra.getText();
        
        if (codigo.equals("") || situacao.equals("") || codFilme.equals("") || precoCompra.equals("") 
                || dataCompra.equals("")) {
            
            JOptionPane.showMessageDialog(null, "Nenhum campo pode estar vazio", "Video Locadora", JOptionPane.WARNING_MESSAGE);
            
        } else {
            
            Connection con = (Connection) Conexao.AbrirConexao();
            DVDDAO sql = new DVDDAO(con);
            int cod = Integer.parseInt(codigo);
            DVD a = new DVD();
            
            a.setCodigo(cod);
            a.setSituacao(situacao);
            a.setCodigoFilme(Integer.parseInt(codFilme));
            a.setPreco(Double.parseDouble(precoCompra));
            a.setDataCompra(dataCompra);
            
            sql.Alterar_DVD(a);
            Conexao.FecharConexao(con);
            
            Limpar();
            
            JOptionPane.showMessageDialog(null, "Cadastro Realizado com Sucesso", "Video Locadora", JOptionPane.INFORMATION_MESSAGE);
           new Menu().setVisible(true);
            dispose();
            
        }
        
    }//GEN-LAST:event_jButton16ActionPerformed

